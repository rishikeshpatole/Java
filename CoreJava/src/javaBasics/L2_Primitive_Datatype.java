package javaBasics;

public class L2_Primitive_Datatype {

	public static void main(String[] args) 
	{
		//Numeric
			//Byte
			byte b= 127;
			byte b1= -128;
			System.out.println("Byte range between "+b1+" to "+b);
		
			//short
			short s= -32768;
			short s1= 32767;
			System.out.println("Short range between "+s+" to "+s1);
			
			//integer
			int i=-2147483648;
			int i1= 2147483647;
			System.out.println("interger range between "+i+" to "+i1);
			
			//long
			long l= -99954540;
			long l1= 999999999;
			System.out.println("long range between "+l+" to "+l1);
			
			//float
			float f= 65465.5545f;
			
			System.out.println("float = "+f);
			
			//double
			double d= 999999999999999999.99999999999d;
			System.out.println("double = "+d);
			
		//char
			char c='c';
			System.out.println("char c = "+c);
			
		//boolean
			boolean B = true;
			System.out.println("Boolean ="+B);
					
	}

}
