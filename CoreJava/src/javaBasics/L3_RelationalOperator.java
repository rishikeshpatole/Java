package javaBasics;

public class L3_RelationalOperator {

	public static void main(String[] args) 
	{
		int a=4;
		int b=2;
		int c=4;
		
		//greater than
		System.out.println("a > b = "+(a>b));
		
		//greater than equal to 
		System.out.println("a >= b = "+(a>=b));
		
		//less than
		System.out.println("a < b = "+(a<b));
	
		//less than equal to 
		System.out.println("a <= b = "+(a<=b));
	
		//equals ==
		System.out.println("a==b = "+(a==b));
		
		//not equals to
		System.out.println("a!=b = "+(a!=b));
	}

}
